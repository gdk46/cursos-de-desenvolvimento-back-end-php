<?php

class Operacoes
{


    public function soma(float $a, float $b)
    {
        return $a + $b;
    }


    public function subtrair(float $a, float $b)
    {
        return $a - $b;
    }


    public function divir(float $a, float $b)
    {
        return $a / $b;
    }

    public function mutiplica(float $a, float $b)
    {
        return $a * $b;
    }
}